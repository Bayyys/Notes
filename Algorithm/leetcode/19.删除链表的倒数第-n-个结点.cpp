/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution
{
public:
    ListNode *removeNthFromEnd(ListNode *head, int n)
    {
        ListNode *ehead = new ListNode(0);
        ehead->next = head;
        ListNode *dp = ehead, *tp = head;
        while (n-- && tp != nullptr)
        {
            tp = tp->next;
        }
        while (tp != nullptr)
        {
            dp = dp->next;
            tp = tp->next;
        }
        ListNode *tmp = dp->next;
        dp->next = dp->next->next;
        delete tmp;
        return ehead->next;
    }
};
// @lc code=end

